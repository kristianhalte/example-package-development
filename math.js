export let addition = (x, y) => {
  return x + y
};

export let subtraction = (x, y) => {
  return x - y
};

export let multiplication = (x, y) => {
  return x * y
};

export let division = (x, y) => {
  return x / y
};

export let triplication = x => {
  return x * 3
};

export let quadruplication = x => {
  return x * 5
};